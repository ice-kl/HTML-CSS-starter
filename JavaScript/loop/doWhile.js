// Do...while ใช้ในกรณีที่อยากให้ลองทำก่อน 1 รอบ แล้วทำซ้ำไปเรื่อยๆตราบเท่าที่เงื่อนไขเป็นจริง
/* การ loop โดยจะทำคำสั่ง do ก่อน 1 รอบ จากนั้นค่อยมาเช็ค while
do{
    คำสั่งเมื่อเงื่อนไขเป็นจริง
}while(เงื่อนไข);
*/

let count = 1;

do{
    console.log(count);
    count++;
}while(count<=5);